    private void btnAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAbrirActionPerformed
        JFileChooser fileChooser = new JFileChooser(".");
        fileChooser.showOpenDialog(this);
        
        try{
            File f = fileChooser.getSelectedFile();
            if (f==null)
                return;
            
            Scanner scn;       
            scn = new Scanner(f);
            String s="";
            while (scn.hasNext())
                s += scn.nextLine() + "\n";                
            
            textArea.setText(s);
            textArea.setCaretPosition(0);
        }catch(Exception e) {};
    }//GEN-LAST:event_btnAbrirActionPerformed

    private void mostrarResults(){
        labelLexema.setText("Lexema: "+analex.lexema());
        labelPreanalisis.setText("Preanalisis: "+analex.preanalisis());
        resaltar(analex.getPosLexema(), analex.lexema().length());
        
        if (errorMgr.hayError()){
            labelError.setText("Error: "+errorMgr.getErrorMsj());
            btnAvanzar.setEnabled(false);
        }
    }
    
    private void setEventosModificacion(JTextArea textArea){
        textArea.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void removeUpdate(DocumentEvent e) {
                onModificacion();
            }

            @Override
            public void insertUpdate(DocumentEvent e) {
                onModificacion();
            }

            @Override
            public void changedUpdate(DocumentEvent arg0) {
                onModificacion();
            }
        });
    }
    
    private void onModificacion(){
        labelLexema.setText("");
        labelPreanalisis.setText("");
        labelError.setText("");
        btnAvanzar.setEnabled(false);
        desResaltar();
    }
